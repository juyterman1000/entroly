"""Query-conditioned compression eval — recall AND precision (discrimination).

NOT the circular marker-recovery test. Here each query has a natural-language
answer planted in a haystack of unrelated paragraphs. We measure, per query:

  recall    : is THIS query's answer kept?            (don't lose the answer)
  precision : are OTHER queries' answers dropped?     (don't waste budget on
                                                        irrelevant content)

A query-agnostic compressor produces the SAME output regardless of query, so it
cannot discriminate — its precision is structurally ~1/n. Query-conditioning is
the only way to get high recall AND high precision at aggressive budgets. That
gap is the whole point, and it is impossible to fake with token heuristics.
"""
from __future__ import annotations

from entroly_core import py_compress_query, py_compress_text

# (query, answer-paragraph, unique marker to detect the answer in output)
TOPICS = [
    ("how does the rate limiter handle burst traffic",
     "The rate limiter uses a token-bucket algorithm. Burst traffic up to five "
     "thousand requests is absorbed by the bucket capacity, and tokens refill at "
     "one hundred per second; excess requests receive HTTP 429.",
     "token-bucket"),
    ("what retry policy does the payment service use for failed charges",
     "The payment service retries failed charges against the Stripe API using "
     "exponential backoff, attempting three times before marking the transaction "
     "permanently failed and emitting a dead-letter event.",
     "exponential backoff"),
    ("how are database migrations applied on deploy",
     "Database migrations run automatically on deploy via the Alembic runner, "
     "which acquires an advisory lock, applies pending revisions in order, and "
     "records each version in the schema_migrations table.",
     "Alembic runner"),
    ("how is user authentication session state stored",
     "Authentication sessions are stored as signed JWT access tokens with a "
     "fifteen-minute expiry, paired with rotating refresh tokens kept in Redis "
     "under a per-device key.",
     "rotating refresh tokens"),
    ("how does the search index get updated when documents change",
     "When a document changes, a change-data-capture stream publishes the delta "
     "to a Kafka topic, and an indexer consumer upserts the affected records into "
     "the OpenSearch cluster within two seconds.",
     "change-data-capture"),
]

# DENSE technical distractors on unrelated topics. The key control: these are
# just as information-rich as the answer paragraphs, so a density/entropy-based
# (query-agnostic) selector cannot tell them apart from the real answer — only
# query relevance can. This is what makes the eval discriminating rather than
# trivially solvable by "keep the densest paragraph".
_DISTRACTOR_TOPICS = [
    "The connection pool maintains {n} warm sockets with a LIFO checkout strategy and a 30-second idle reaper.",
    "Garbage collection uses a generational copying collector with a {n}-megabyte eden space and tenuring threshold 8.",
    "The TLS terminator pins ECDSA P-256 certificates and rotates session tickets every {n} minutes via the keyless server.",
    "Shard rebalancing moves virtual nodes across {n} physical hosts using consistent hashing with bounded-load replicas.",
    "The write-ahead log fsyncs every {n} milliseconds and checkpoints when the segment exceeds 64 megabytes.",
    "Image thumbnails are generated by a Lanczos resampler at {n} pixels and cached in a content-addressed blob store.",
    "The feature-flag service evaluates {n} rules per request through a compiled decision tree with percentage rollouts.",
    "Backpressure is applied by a leaky-bucket admission controller draining at {n} tokens per second per tenant.",
    "The metrics pipeline aggregates histograms with {n} exponential buckets and ships them over OTLP every 10 seconds.",
    "Cold storage tiering moves objects older than {n} days from SSD to erasure-coded archive with a 12-hour restore SLA.",
]
HAYSTACK = [
    _DISTRACTOR_TOPICS[i % len(_DISTRACTOR_TOPICS)].format(n=(i * 37) % 900 + 10)
    + f" (distinct deployment region {i} with its own tuning constants)"
    for i in range(45)
]


def build_doc() -> str:
    paras = list(HAYSTACK)
    # Interleave the five answers at spread positions.
    for k, (_, ans, _) in enumerate(TOPICS):
        paras.insert((k + 1) * 8, ans)
    return "\n\n".join(paras)


def run():
    doc = build_doc()
    toks = len(doc) // 4
    markers = [m for _, _, m in TOPICS]

    for budget in (300, 120, 60):
        print(f"\n=== doc={toks} tokens, budget={budget} ({100*budget//toks}% of input) ===")
        print(f"{'query topic':32} | {'QC recall':9} {'QC precision':12} | {'agnostic recall':15} {'agnostic prec':13}")
        print("-" * 100)

        # Query-agnostic output is the SAME for every query.
        agnostic = py_compress_text(doc, budget)
        ag_kept = [m for m in markers if m in agnostic]

        qc_recall_hits = 0
        qc_prec_num = 0
        qc_prec_den = 0
        for (query, _, marker) in TOPICS:
            qc = py_compress_query(doc, query, budget)
            qc_kept = [m for m in markers if m in qc]
            recall = marker in qc
            # precision: of the answer-paragraphs kept, how many are the right one
            prec = (1.0 / len(qc_kept)) if qc_kept and marker in qc_kept else 0.0
            qc_recall_hits += int(recall)
            if qc_kept:
                qc_prec_num += (1 if marker in qc_kept else 0)
                qc_prec_den += len(qc_kept)

            ag_recall = marker in agnostic
            ag_prec = (1.0 / len(ag_kept)) if ag_kept and marker in ag_kept else 0.0
            print(f"{query[:32]:32} | {'YES' if recall else 'no':9} {prec:<12.2f} | "
                  f"{'YES' if ag_recall else 'no':15} {ag_prec:<13.2f}")

        qc_prec = (qc_prec_num / qc_prec_den) if qc_prec_den else 0.0
        len(ag_kept) and sum(1 for _,_,m in TOPICS if m in agnostic) / len(TOPICS)
        print("-" * 100)
        print(f"  QUERY-CONDITIONED : recall {qc_recall_hits}/{len(TOPICS)},  precision {qc_prec:.2f}")
        print(f"  QUERY-AGNOSTIC    : recall {sum(1 for m in markers if m in agnostic)}/{len(TOPICS)},  "
              f"precision {(len(ag_kept) and 1/len(ag_kept)) or 0:.2f}  (same output for every query)")


if __name__ == "__main__":
    run()
